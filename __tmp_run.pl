sub complex_function {
    my $-a;
    my $i = "0";
while (     system 'unknown_command';
 ) {
if ($args{i} =~ /^--.*$/msx) {
                        my $key = ""${args[i]}"";
                        my $value = ""${args[i+1]}"";
                        $options{"$key"} = "$value";
                        system 'unknown_command';
        } elsif ($args{i} =~ /^-.*$/msx) {
                        my $flags = ""${args[i]}"";
                        my $j = "0";
            while (             system 'unknown_command';
 ) {
                $options{"${flags:j:1}"} = "true";
                system 'unknown_command';
            }
                        system 'unknown_command';
        } elsif ($args{i} =~ /^.*$/msx) {
            last;        }
    }
    print "Processed " . scalar(@options) . " options\n";
    return;
}
complex_function('--option1=value1', '-abc');

exit $main_exit_code;
